from sys import stderr
from itertools import chain
from textwrap import indent
from operator import attrgetter

from caruca.annotator.posh import PoshSpecification
from pydantic import BaseModel

from caruca.annotator.pash import Parallelizability, PaShSpecification, Predicate
from caruca.annotator.sash import (
    FileSystemPredicate,
    SashRunSpecification,
    SaShSpecification,
)
from caruca.annotator.shellcheck import ShellCheckPattern


class RunSpecification(BaseModel):
    """
    Specification of one run of a command with a given file system environment.
    """

    inputs: list[str]
    outputs: list[str]
    return_code: int | None
    parallelizability: Parallelizability
    input_split: bool
    args_split: bool
    input_geq_output: bool | None
    preconditions: list[FileSystemPredicate]
    postconditions: list[FileSystemPredicate]
    updates: list[str]
    deletions: list[str] = []

    def to_human(self) -> str:
        return "\n".join(f"{k}: {v}" for k, v in self.model_dump())

    @property
    def successful(self):
        return self.return_code == 0 and ("stderr" not in self.outputs)

    def to_sash(self):
        return SashRunSpecification(
            preconditions=self.preconditions,
            postconditions=self.postconditions,
            updates=self.updates,
            return_code=self.return_code,
        )


class Specification(BaseModel):
    """
    Specification of one invocation of a command.
    Contains multiple RunSpecifications, since the same invocation is run multiple time with different environments
    """

    invocation: dict
    true_str: str
    annotations: list[RunSpecification]

    def to_human(self) -> str:
        body = "\n".join([indent(el.to_human(), " " * 4) for el in self.annotations])
        return f"Invocation: {self.true_str}\nConditions:" + (
            f"\n{body}" if body else "no successful runs"
        )

    def to_posh(self) -> PoshSpecification:
        return PoshSpecification(
            input_split=any(map(attrgetter("input_split"), self.annotations)),
            args_split=any(map(attrgetter("args_split"), self.annotations)),
            input_geq_output=any(map(attrgetter("input_geq_output"), self.annotations)),
        )

    def to_sash(self) -> SaShSpecification:
        return SaShSpecification([ann.to_sash() for ann in self.annotations])

    def to_pash(self) -> PaShSpecification | None:
        if self.invocation["body"]:
            flag_predicate = Predicate(
                operator="exists",
                operands=[
                    arg["flag"] for arg in self.invocation["body"] if arg["flag"]
                ],
            )
            operand_len_predicate = Predicate(
                operator="len_args_eq",
                operands=[
                    len([arg for arg in self.invocation["body"] if not arg["flag"]])
                ],
            )

            predicate = Predicate(
                operator="and",
                operands=[
                    flag_predicate,
                    operand_len_predicate,
                ],
            )

        else:
            predicate = Predicate(
                operator="and",
                operands=[
                    Predicate(operator="exists", operands=[]),
                    Predicate(operator="len_args_eq", operands=[0]),
                ],
            )

        inputs = set(
            inp.removesuffix(".jpg")
            for inp in chain.from_iterable(
                ann.inputs for ann in self.annotations if ann.successful
            )
        )
        outputs = set(
            chain.from_iterable(
                ann.outputs for ann in self.annotations if ann.successful
            )
        )

        pclass = max(
            (ann.parallelizability for ann in self.annotations if ann.successful),
            default=Parallelizability.E,
        )

        non_flag_args = [
            arg["value"]
            for item in self.invocation["body"]
            for arg in item["args"]
            if not item["flag"]
        ]

        inputs = set(
            f"args[{non_flag_args.index(inp)}]" if inp in non_flag_args else inp
            for inp in inputs
        )

        outputs = set(
            f"args[{non_flag_args.index(out)}]" if out in non_flag_args else out
            for out in outputs
        )

        if not any(ann.successful for ann in self.annotations):
            print(f"No successful runs found for {self.true_str}", file=stderr)
            return

        return PaShSpecification(
            predicate=predicate,
            pclass=pclass,
            inputs=list(inputs),
            outputs=list(outputs),
            true_str=self.true_str,
            comments="Automatically generated by caruca.",
        )

    def to_shellcheck(self) -> ShellCheckPattern | None:
        deletions = set(
            deletion
            for ann in self.annotations
            if ann.successful
            for deletion in ann.deletions
        )

        if not deletions:
            return None

        if self.invocation["body"]:
            flag_predicate = Predicate(
                operator="exists",
                operands=[
                    arg["flag"] for arg in self.invocation["body"] if arg["flag"]
                ],
            )
            operand_len_predicate = Predicate(
                operator="len_args_eq",
                operands=[
                    len([arg for arg in self.invocation["body"] if not arg["flag"]])
                ],
            )

            predicate = Predicate(
                operator="and",
                operands=[
                    flag_predicate,
                    operand_len_predicate,
                ],
            )

        else:
            predicate = Predicate(
                operator="and",
                operands=[
                    Predicate(operator="exists", operands=[]),
                    Predicate(operator="len_args_eq", operands=[0]),
                ],
            )

        non_flag_args = [
            arg["value"]
            for item in self.invocation["body"]
            for arg in item["args"]
            if not item["flag"]
        ]

        deletions = set(
            f"args[{non_flag_args.index(target)}]"
            if target in non_flag_args
            else target
            for target in deletions
        )

        return ShellCheckPattern(
            command=self.invocation["name"],
            predicate=predicate,
            deletions=sorted(deletions),
            true_strs=[self.true_str],
        )
