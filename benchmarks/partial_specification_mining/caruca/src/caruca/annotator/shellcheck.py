from typing import Literal

from pydantic import BaseModel

from caruca.annotator.pash import Predicate


class ShellCheckPattern(BaseModel):
    command: str
    predicate: Predicate | Literal["default"]
    deletions: list[str]
    true_strs: list[str]


class MissingDestinationCheck(BaseModel):
    command: str
    required_paths: int
    target_directory_flags: list[str]


class ShellCheck(BaseModel):
    command: str
    cases: list[ShellCheckPattern]
    missing_destination: MissingDestinationCheck | None = None
    code: str


def _hs_list(items: list[str]) -> str:
    escaped = [item.replace("\\", "\\\\").replace('"', '\\"') for item in items]
    return "[" + ", ".join(f'"{el}"' for el in escaped) + "]"


def _predicate_to_hs(predicate: Predicate | Literal["default"]) -> str:
    if predicate == "default":
        return "\\_ -> True"

    def render(pred: Predicate) -> str:
        match pred.operator:
            case "exists":
                return f"hasFlags {_hs_list(list(pred.operands))}"
            case "len_args_eq":
                return f"lenArgsEq {pred.operands[0]}"
            case "and":
                parts = []
                for op in pred.operands:
                    if isinstance(op, Predicate):
                        parts.append(render(op))
                if not parts:
                    return "\\_ -> True"
                return " \\c -> " + " && ".join(f"({p} c)" for p in parts)
            case _:
                return "\\_ -> True"

    rendered = render(predicate)  # type: ignore[arg-type]
    if rendered.startswith(" \\c ->"):
        return rendered
    return f"\\c -> ({rendered}) c"


def _render_missing_destination(check: MissingDestinationCheck) -> str:
    flags = _hs_list(check.target_directory_flags)
    return f"""hasAnyFlagWithPrefix :: CommandLike c => [String] -> c -> Bool
hasAnyFlagWithPrefix prefixes c = any (\\p -> any (isPrefixOf p) (commandFlags c)) prefixes

isStaticArg :: String -> Bool
isStaticArg arg = all (`notElem` ("$*?[{{" :: String)) arg

checkCarucaMissingDestination :: CommandLike c => c -> [String]
checkCarucaMissingDestination cmd =
  [ "SC-CARUCA-MISS-DEST: " ++ "{check.command}" ++ " has no destination. Check the arguments."
  | commandName cmd == "{check.command}"
  , length (commandArgs cmd) < {check.required_paths}
  , length (commandArgs cmd) > 0
  , null (commandFlags cmd)
  , all isStaticArg (commandArgs cmd)
  , not (hasAnyFlagWithPrefix {flags} cmd)
  ]

"""


def render_shellcheck_module(
    command: str,
    patterns: list[ShellCheckPattern],
    missing_destination: MissingDestinationCheck | None = None,
) -> str:
    header = f"""-- Generated by caruca. Paste into ShellCheck's checks module.
-- Command: {command}
module Caruca.DangerousDelete where

import Data.List (intercalate, isPrefixOf)

class CommandLike c where
  commandName :: c -> String
  commandArgs :: c -> [String]
  commandFlags :: c -> [String]

hasFlags :: CommandLike c => [String] -> c -> Bool
hasFlags flgs c = all (`elem` commandFlags c) flgs

lenArgsEq :: CommandLike c => Int -> c -> Bool
lenArgsEq n c = length (commandArgs c) == n

data DeletePattern c = DeletePattern
  {{ dpName :: String
  , dpMatch :: c -> Bool
  , dpDeletes :: [String]
  , dpExamples :: [String]
  }}

"""

    entries = []
    for pat in patterns:
        pred_expr = _predicate_to_hs(pat.predicate)
        deletions = _hs_list(pat.deletions)
        examples = _hs_list(pat.true_strs)
        entries.append(
            f'  DeletePattern "{pat.command}" ({pred_expr}) {deletions} {examples}'
        )

    patterns_block = "deletePatterns :: CommandLike c => [DeletePattern c]\n"
    patterns_block += "deletePatterns =\n  [\n"
    patterns_block += ",\n".join(entries)
    patterns_block += "\n  ]\n\n"

    check_fn = """checkCarucaDangerousDelete :: CommandLike c => c -> [String]
checkCarucaDangerousDelete cmd =
  [ "SC-CARUCA-DANG-DELETE: " ++ dpName p
        ++ " may delete " ++ intercalate ", " (dpDeletes p)
        ++ " (e.g. " ++ intercalate " | " (dpExamples p) ++ ")"
  | p <- deletePatterns
  , commandName cmd == dpName p
  , dpMatch p cmd
  ]

"""

    missing_dest_block = (
        _render_missing_destination(missing_destination) if missing_destination else ""
    )

    combined_fn = """checkCaruca :: CommandLike c => c -> [String]
checkCaruca cmd = checkCarucaDangerousDelete cmd ++ checkCarucaMissingDestination cmd
"""

    if not missing_destination:
        combined_fn = ""

    return header + patterns_block + check_fn + missing_dest_block + combined_fn
